#!/usr/bin/env node
/**
 * Sprint 6.5 — generate TypeScript types from committed OpenAPI specs.
 *
 * Reads ``docs/api-specs/*.openapi.json`` (refreshed by
 * ``scripts/dump_openapi.py``) and writes types-only ``.d.ts`` files under
 * ``frontend/lib/api/types/``.
 *
 * Why types-only (NOT orval / hooks generation):
 *   * Phase 1 wants the types contract to be source-of-truth without
 *     locking us into a specific HTTP client wrapper. ``openapi-typescript``
 *     produces zero runtime code — just the ``paths`` + ``components``
 *     interfaces — so our existing ``api()`` helper + react-query stays
 *     unchanged.
 *   * Upgrading to orval (or react-query-codegen) is a Phase 2 call once
 *     we hit the size where hand-writing fetch wrappers per endpoint
 *     becomes friction.
 *
 * Run ``npm run gen:api`` after ``python scripts/dump_openapi.py`` from the
 * repo root. CI uses ``--check`` to fail when committed types drift.
 */
import fs from "node:fs/promises";
import path from "node:path";
import url from "node:url";
import openapiTS, { astToString } from "openapi-typescript";

const HERE = path.dirname(url.fileURLToPath(import.meta.url));
const REPO = path.resolve(HERE, "..", "..");
const SPECS_DIR = path.join(REPO, "docs", "api-specs");
const TYPES_DIR = path.join(REPO, "frontend", "lib", "api", "types");

const SERVICES = [
  { name: "pipeline",     spec: "pipeline.openapi.json"     },
  { name: "orchestrator", spec: "orchestrator.openapi.json" },
  // auth: spec is generated at runtime via springdoc — see API_CODEGEN.md
  // for the curl recipe to refresh docs/api-specs/auth.openapi.json. Once
  // that file exists, add `{ name: "auth", spec: "auth.openapi.json" }` here.
];

const args = process.argv.slice(2);
const checkOnly = args.includes("--check");

async function generate(name, spec) {
  const specPath  = path.join(SPECS_DIR, spec);
  const outPath   = path.join(TYPES_DIR, `${name}.d.ts`);

  // Ensure spec exists before invoking openapi-typescript so the error
  // is friendly instead of a stack from the lib.
  try {
    await fs.access(specPath);
  } catch {
    throw new Error(
      `Missing spec ${path.relative(REPO, specPath)} — run ` +
      `\`python scripts/dump_openapi.py ${name}\` from the repo root first.`,
    );
  }

  const ast      = await openapiTS(new URL(`file://${specPath}`));
  const banner   =
    `/**\n` +
    ` * AUTO-GENERATED by frontend/scripts/gen-api-types.mjs\n` +
    ` * Source: docs/api-specs/${spec}\n` +
    ` * DO NOT EDIT BY HAND — run \`npm run gen:api\` after the BE spec changes.\n` +
    ` */\n\n`;
  const contents = banner + astToString(ast);

  if (checkOnly) {
    let prev = "";
    try { prev = await fs.readFile(outPath, "utf8"); } catch { /* file missing */ }
    if (prev !== contents) {
      console.error(
        `[${name}] STALE — frontend/lib/api/types/${name}.d.ts out of date. ` +
        `Run \`npm run gen:api\`.`,
      );
      return false;
    }
    console.log(`[${name}] OK — types match committed spec`);
    return true;
  }

  await fs.mkdir(TYPES_DIR, { recursive: true });
  await fs.writeFile(outPath, contents, "utf8");
  console.log(
    `[${name}] wrote frontend/lib/api/types/${name}.d.ts ` +
    `(${contents.split("\n").length} lines)`,
  );
  return true;
}

let bad = 0;
for (const svc of SERVICES) {
  try {
    const ok = await generate(svc.name, svc.spec);
    if (!ok) bad += 1;
  } catch (err) {
    console.error(`[${svc.name}] FAILED: ${err.message}`);
    bad += 1;
  }
}

process.exit(bad > 0 ? 1 : 0);
