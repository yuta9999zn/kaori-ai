const DICTS: Record<Locale, Dict> = { vi, en, ja, ko, zh };

export function translate(
  key: string,
  locale: Locale,
  params?: Record<string, string | number>,
): string {
  const raw =
    DICTS[locale]?.[key] ??
    DICTS['en']?.[key] ??
    key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}
