"""Shape tests for migrations 053 + 054 — workflow builder.

053 = workflows + workflow_nodes + workflow_edges + workflow_step_documents
      + workflow_templates tables with RLS K-1 + ABAC dept_scope.
054 = auto-generated by scripts/generate_workflow_templates_migration.py —
      18 templates × ~5 cards. We verify the generator output structure
      (template count, dept coverage, card schema completeness).

Pure-Python (no DB). Pattern mirrors test_migrations_051_052_shape.py.
Run from repo root:  python -m pytest scripts/test_migrations_053_054_shape.py
"""
from __future__ import annotations

import json
import re
import sys
from pathlib import Path

import pytest

REPO = Path(__file__).resolve().parent.parent
MIG_DIR = REPO / "infrastructure" / "postgres" / "migrations"
sys.path.insert(0, str(REPO / "scripts"))

MIG_053_RAW = (MIG_DIR / "053_workflow_builder_tables.sql").read_text(encoding="utf-8")
MIG_054_RAW = (MIG_DIR / "054_workflow_templates_seed.sql").read_text(encoding="utf-8")


# ─── Mig 053 — table creation ───────────────────────────────────────────


EXPECTED_TABLES = [
    "workflows",
    "workflow_nodes",
    "workflow_edges",
    "workflow_step_documents",
    "workflow_templates",
]


@pytest.mark.parametrize("table", EXPECTED_TABLES)
def test_mig_053_creates_table(table):
    assert f"CREATE TABLE IF NOT EXISTS {table}" in MIG_053_RAW, (
        f"Mig 053 missing CREATE TABLE for {table}"
    )


@pytest.mark.parametrize("table", [
    "workflows", "workflow_nodes", "workflow_step_documents"
])
def test_mig_053_enterprise_scoped_table_has_enterprise_id(table):
    """Per-tenant tables must carry enterprise_id for RLS K-1."""
    idx = MIG_053_RAW.index(f"CREATE TABLE IF NOT EXISTS {table}")
    body = MIG_053_RAW[idx:idx + 5000]
    assert "enterprise_id" in body, f"{table} missing enterprise_id"


