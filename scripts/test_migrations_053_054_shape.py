def test_mig_054_three_templates_per_dept():
    """Anh's directive: 3 workflows per dept (c/d/e)."""
    from generate_workflow_templates_migration import ALL_TEMPLATES  # noqa: PLC0415
    by_dept: dict[str, int] = {}
